Cypress.Commands.add('loginWithUi',(username, password) => {
    cy.log('type in user')
    cy.get('#user-name').type(username)
    cy.log('Type in password')
    cy.get('[placeholder=Password]').type(password)
    cy.log('Clicking log in')
    cy.get('.btn_action').click()
})